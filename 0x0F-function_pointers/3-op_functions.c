/**
 * op_add - calculates the sum of two
 * numbers
 * @a: first number
 * @b: second number
 * Return: the sum of the two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - calculates the difference
 * between the two numbers
 * @a: first number
 * @b: second number
 * Return: the difference between the
 * two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - calculates the product of
 * two numbers
 * @a: first number
 * @b: second number
 * Return: the product of the two
 * numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - calculates the division of
 * two numbers
 * @a: dividend
 * @b: divisor
 * Return: the quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - calculated the remainder of
 * the division of two numbers (modulo)
 * @a: the dividend
 * @b: the divisor
 * Return: the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
